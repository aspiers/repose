package org.openrepose.core.services.reporting.metrics;

import com.codahale.metrics.MetricRegistry;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.EnableAspectJAutoProxy;

/**
 * Creating a {{@link MetricRegistry}} bean and enabling auto-proxying
 * for AOP support.
 *
 * Since {{@link MetricRegistry}} is an implementation class, and does
 * not implement a useful interface, proxyTargetClass is being set to
 * true to force GCLIB proxyies instead of relying on JDK dynamic proxies.
 */
@Configuration
@EnableAspectJAutoProxy(proxyTargetClass = true)
public class MetricRegistryConfig {

    @Bean
    public MetricRegistry metricRegistry() {
        return new MetricRegistry();
    }
}
